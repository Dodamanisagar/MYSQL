-- ABS function
SELECT ABS(-5); -- Output: 5

-- CEIL / CEILING function
SELECT CEIL(3.7); -- Output: 4

-- FLOOR function
SELECT FLOOR(5.8); -- Output: 5

-- ROUND function
SELECT ROUND(3.14159, 2); -- Output: 3.14

-- MOD function
SELECT MOD(10, 3); -- Output: 1

-- POWER / POW function
SELECT POWER(2, 3); -- Output: 8

-- SQRT function
SELECT SQRT(25); -- Output: 5

-- EXP function
SELECT EXP(2); -- Output: 7.3890560989306495

-- LOG function
SELECT LOG(10); -- Output: 2.302585092994046

-- LOG10 function
SELECT LOG10(100); -- Output: 2

-- RAND function
SELECT RAND(); -- Output: Random floating-point number between 0 and 1

-- TRUNCATE function
SELECT TRUNCATE(3.14159, 2); -- Output: 3.14

-- SIGN function
SELECT SIGN(-10); -- Output: -1

-- PI function
SELECT PI(); -- Output: 3.141592653589793

-- RADIANS / DEGREES function
SELECT RADIANS(180); -- Output: 3.141592653589793
